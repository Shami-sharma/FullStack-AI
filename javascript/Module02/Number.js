let  num1 = 43;
let num2 = 3.14;

// 3 ways to add numbers

// 1 using + operator
console.log(num1 + num2);

// 2 using parseInt() method
console.log(parseInt(num1) + parseInt(num2));

// 3 using parseFloat() method
console.log(parseFloat(num1) + parseFloat(num2));

// 3 ways to subtract numbers

// 1 using - operator
console.log(num1 - num2);           

// 2 using parseInt() method
console.log(parseInt(num1) - parseInt(num2));

// 3 using parseFloat() method
console.log(parseFloat(num1) - parseFloat(num2));

// 3 ways to multiply numbers

// 1 using * operator
console.log(num1 * num2);

// 2 using parseInt() method
console.log(parseInt(num1) * parseInt(num2));

// 3 using parseFloat() method
console.log(parseFloat(num1) * parseFloat(num2));


// 3 ways to divide numbers

// 1 using / operator
console.log(num1 / num2);

// 2 using parseInt() method
console.log(parseInt(num1) / parseInt(num2));

// 3 using parseFloat() method
console.log(parseFloat(num1) / parseFloat(num2));

// 3 ways to find remainder of numbers

// 1 using % operator
console.log(num1 % num2);

// 2 using parseInt() method
console.log(parseInt(num1) % parseInt(num2));

// 3 using parseFloat() method
console.log(parseFloat(num1) % parseFloat(num2));   


// 3 ways to find power of numbers

// 1 using ** operator
console.log(num1 ** num2);

// 2 using Math.pow() method
console.log(Math.pow(num1, num2));


// 3 using parseInt() method
console.log(parseInt(num1) ** parseInt(num2));

// 3 ways to find square root of numbers

// 1 using Math.sqrt() method
console.log(Math.sqrt(num1));


// 2 using parseInt() method
console.log(Math.sqrt(parseInt(num1)));

// 3 using parseFloat() method
console.log(Math.sqrt(parseFloat(num1)));

// 3 ways to find absolute value of numbers

// 1 using Math.abs() method
console.log(Math.abs(num1));

// 2 using parseInt() method
console.log(Math.abs(parseInt(num1)));

// 3 using parseFloat() method
console.log(Math.abs(parseFloat(num1)));

// 3 ways to find maximum of numbers

// 1 using Math.max() method
console.log(Math.max(num1, num2));

// 2 using parseInt() method
console.log(Math.max(parseInt(num1), parseInt(num2)));  

// 3 using parseFloat() method
console.log(Math.max(parseFloat(num1), parseFloat(num2)));

// 3 ways to find minimum of numbers

// 1 using Math.min() method
console.log(Math.min(num1, num2));

// 2 using parseInt() method
console.log(Math.min(parseInt(num1), parseInt(num2)));

// 3 using parseFloat() method
console.log(Math.min(parseFloat(num1), parseFloat(num2)));

// 3 ways to round numbers

// 1 using Math.round() method
console.log(Math.round(num1));

// 2 using parseInt() method
console.log(Math.round(parseInt(num1)));

// 3 using parseFloat() method
console.log(Math.round(parseFloat(num1)));

